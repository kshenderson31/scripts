my $dbh = DBI->connect('dbi:Oracle:host=paradies;sid=genret;port=1521', 'system', 'genret',{RaiseError => 1,AutoCommit => 0}) || die "Database connection not made: $DBI::errstr";
my $sth;
my $sql;

print "=================================================\n";
print "Processing Users\n";
print "=================================================\n";

while(<DATA>)
{
    chomp($_);
    my($user, $status) = split(",", $_);

    $user=uc $user;
    $status=uc $status;

    $sth = $dbh->prepare("ALTER USER $user ACCOUNT $status");
    $sth->execute();
    
    if($status eq "LOCK")
    {
	    $sth = $dbh->prepare("ALTER USER $user ACCOUNT $status");
	    $sth->execute();
    }

    print "User $user($status)\n";
}

$sql = <<__SQL__;
SELECT s.sid,
       s.serial#,
       s.username,
       s.program
FROM   gv\$session s,
       gv\$process p,
       dba_users  u 
WHERE p.addr = s.paddr 
  AND p.inst_id = s.inst_id
  AND s.type != 'BACKGROUND'
  AND u.username = s.username
  AND u.account_status = 'LOCKED'
__SQL__

$sth = $dbh->prepare($sql);
$sth->execute();

my($sid, $serial, $username, $program);
$sth->bind_columns(undef, \$sid, \$serial, \$username, \$program);

print "\n\n";
print "=================================================\n";
print "Killing Sessions\n";
print "=================================================\n";

while( $sth->fetch() ) 
{
	print "Killing Session for $username($sid, $serial); $program\n";
	my $kill = $dbh->prepare("ALTER SYSTEM KILL SESSION \'$sid,$serial\' IMMEDIATE");
	$kill->execute();
}

print "=================================================\n";
print "Done\n";
print "=================================================\n";

$sth->finish();
$dbh->disconnect;

